// 组合命名：手工优先 → 同色即纯色 → 两个词根拼成 4 字名（唯一、无“×”）
export function comboName(baseId: string, patId: string): string {
  const key = `${baseId}|${patId}`
  if (COMBO_NAMES[key]) return COMBO_NAMES[key]
  const base = COLOR_BY_ID[baseId], pat = COLOR_BY_ID[patId]
  if (!base || !pat) return '未知'
  if (baseId === patId) return base.name              // 同底同纹 = 纯色
  return `${base.root ?? base.name}${pat.root ?? pat.name}`
}
